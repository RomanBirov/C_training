#include "libft.h"

int	ft_str_is_alpha(char *str)
{
	size_t	i;

	i = 0;
	if (!str)
		return (0);
	while (str[i] != '\0')
	{
		if (!((str[i] >= 65 && str[i] <= 90) ||
					(str[i] >= 97 && str[i] <= 122)))
			return (0);
		i++;
	}
	return (1);
}
