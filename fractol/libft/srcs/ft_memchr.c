#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	*ps;

	ps = (char *)s;
	while (n > 0)
	{
		if (*ps == (char)c)
			return (ps);
		ps++;
		n--;
	}
	return (0);
}
