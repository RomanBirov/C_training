#include "libft.h"

int		ft_isalnum(int c)
{
	if ((c <= 'z' && c >= 'a') || (c <= 'Z' && c >= 'A')
		|| (c > '0' - 1 && c < '9' + 1))
		return (1);
	return (0);
}
