#include "libft.h"

void	ft_putnbr(int n)
{
	long int out;

	out = n;
	if (out < 0)
	{
		write(1, "-", 1);
		out *= -1;
	}
	if (out > 9)
		ft_putnbr(out / 10);
	out = (out % 10) + 48;
	write(1, &out, 1);
}
