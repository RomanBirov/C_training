#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char p;
	char *ps;

	p = (char)c;
	ps = (char *)s;
	while (*ps != p)
	{
		if (*ps == '\0')
			return (0);
		ps++;
	}
	return ((char *)ps);
}
