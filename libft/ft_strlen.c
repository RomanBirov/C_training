#include "libft.h"

size_t	ft_strlen(const char *s)
{
	int i;
	int len;

	i = 0;
	len = 0;
	if (*s == '\0')
		return (0);
	while (s[i] != '\0')
	{
		i++;
		len++;
	}
	return (len);
}
